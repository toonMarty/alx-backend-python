#!/usr/bin/env python3
"""This module contains a function that adds two
float values
"""


def add(a: float, b: float) -> float:
    """
    This function adds two float values, a and b
    that have been annotated
    Args:
        a (float): first value
        b (float): second value
    Return:
        a + b (float): the sum of the float values
    """
    return a + b
