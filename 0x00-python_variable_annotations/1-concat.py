#!/usr/bin/env python3
"""
This module contains a function concat that
concatenates two strings
"""


def concat(str1: str, str2: str) -> str:
    """
    This function takes two strings as arguments and
    returns a concatenated string
    Args:
        str1 (str): first string
        str2 (str): second string
    Return:
         str1 + str2 (str): a concatenation of str1 and str2
    """
    return str1 + str2
