#!/usr/bin/env python3
"""This module contains a function floor that
returns the floor of a float
"""
import math


def floor(n: float) -> int:
    """
    This function takes a float as an argument and
    returns the floor as an int
    Args:
        n (float): the value to find the floor
    Return:
        n (int): the floor of n
    """
    return math.floor(n)
